                    <h3>1. Qu'est-ce qu'un cookie ?</h3>
                    <p>Un cookie est un petit fichier texte déposé sur votre terminal (ordinateur, smartphone, tablette) lors de la visite d'un site web. Il permet au site de mémoriser des informations sur votre visite, comme votre langue préférée et d'autres paramètres, afin de vous faciliter la navigation lors de vos prochaines visites.</p>

                    <h3>2. Les cookies que nous utilisons</h3>
                    <h4>2.1 Cookies strictement nécessaires</h4>
                    <p>Ces cookies sont indispensables au fonctionnement du site. Ils vous permettent d'utiliser les principales fonctionnalités (session sécurisée, authentification, formulaires). Sans ces cookies, certaines parties du site ne peuvent pas fonctionner correctement. <strong>Ces cookies ne peuvent pas être désactivés.</strong></p>

                    <h4>2.2 Cookies de performance et d'analyse</h4>
                    <p>Ces cookies nous permettent de mesurer l'audience du site et d'analyser le comportement des visiteurs dans un but d'amélioration continue. Les données collectées sont anonymisées. Nous utilisons <strong>Google Analytics 4</strong> pour cette finalité, conformément aux recommandations de la CNIL.</p>

                    <h4>2.3 Cookies fonctionnels</h4>
                    <p>Ces cookies permettent de mémoriser vos préférences (langue, mode sombre/clair, devise) afin de personnaliser votre expérience. Ils ne tracent aucune information personnellement identifiable.</p>

                    <h4>2.4 Cookies marketing</h4>
                    <p>Ces cookies sont utilisés pour vous proposer des publicités pertinentes sur d'autres sites web. Ils ne sont déposés qu'avec votre consentement explicite. Vous pouvez retirer ce consentement à tout moment.</p>

                    <h3>3. Durée de conservation</h3>
                    <ul>
                        <li><strong>Cookies de session :</strong> supprimés à la fermeture du navigateur</li>
                        <li><strong>Cookies persistants :</strong> conservés au maximum 13 mois conformément aux recommandations de la CNIL</li>
                        <li><strong>Cookies analytics :</strong> données anonymisées conservées 25 mois maximum</li>
                    </ul>

                    <h3>4. Gestion de vos préférences</h3>
                    <p>Lors de votre première visite sur notre site, une bannière vous informe de la présence de cookies et vous demande votre consentement pour les cookies non essentiels. Vous pouvez modifier vos préférences à tout moment en cliquant sur <strong>"Gérer les cookies"</strong> en bas de page, ou en paramétrant votre navigateur :</p>
